function vlrAte = atenuacionGasesOtra(p,t,densAgua,ro,f)
    rp = p/1013;
    rt = 288/(273+t);
    
    yop=(2.128)*((rp)^(1.4954))*((rt)^(-1.6032))*exp((-2.5280)*(1-rt));
    
    n1=6.7665*((rp)^(-0.5050))*((rt)^(0.5106))*exp((1.5663)*(1-rt))-1;
    n2=27.8843*((rp)^(-0.4988))*((rt)^(0.8491))*exp((0.5496)*(1-rt))-1;
    
    a=(log(n2/n1))/log(3.5);
    b=((4)^a)/n1;
    
    sum1 = (7.34*((rp)^2)*((rt)^3))/((f)^2)+(0.36*((rp)^2)*((rt)^2));
    sum2 = ((0.3429*b*yop)/(((54-f)^a)+b));
    
    yo= (sum1+sum2)*((f)^2)*((10)^(-3))
    
    e1 = (0.9544*rp*(rt^0.69)) + (0.0061*densAgua);
    e2 = (0.95*rp*(rt^0.64)) + (0.0067*densAgua);
    e3 = (0.9561*rp*(rt^0.67)) + (0.0059*densAgua);
    e4 = (0.9543*rp*(rt^0.68)) + (0.0061*densAgua);
    e5 = (0.955*rp*(rt^0.68)) + (0.006*densAgua);
    
    g22 = (1+(f-22.235)^2)/((f+22.235)^2);
    g557 = (1+(f-557)^2)/((f+557)^2);
    g752 = (1+(f-752)^2)/((f+752)^2);
    
    s1 = (3.13*(10^-2)*rp*(rt^2));
    s2 = (1.76*(10^-3)*densAgua*(rt^8.5));
    s3 = (3.84*e1*g22*exp(2.23*(1-rt)))/(((f-22.235)^2)+(9.42*(e1^2)));
    s4 = (10.48*e2*exp(0.7*(1-rt)))/(((f-183.31)^2)+(9.48*(e2^2)));
    s5 = (0.078*e3*exp(6.4385*(1-rt)))/(((f-321.226)^2)+(6.29*(e3^2)));
    s6 = (3.76*e4*exp(1.6*(1-rt)))/(((f-325.153)^2)+(9.22*(e4^2)));
    s7 = (26.36*e5*exp(1.09*(1-rt)))/(((f-380)^2));
    s8 = (17.87*e5*exp(1.46*(1-rt)))/(((f-448)^2));
    s9 = (883.7*e5*g557*exp(0.17*(1-rt)))/(((f-557)^2));
    s10 = (302.6*e5*g752*exp(0.41*(1-rt)))/(((f-752)^2));
    s11 = (f^2)*densAgua*(10^-4);
    
    yw = (s1+s2+(rt^2.5)*(s3+s4+s5+s6+s7+s8+s9+s10))*s11
    
    ho = 5.386-(3.32734*10^-2)*f + ((1.87185*10-3)*f^2)- ((3.52087*10^-5)*f^3)+ (83.26/((f-60)^2+1.2))
    
    hw = 1.65*(1+(1.61/((f-22.32)^2+2.91))+(3.33/(f-183.3)^2)+(1.9/((f-325.1)^2+3.34))) 
    
    vlrAte = yo*ho + yw*hw;     %Atenuacion total por gases
    %vlrAte = vlrAte/sind(ro);
end